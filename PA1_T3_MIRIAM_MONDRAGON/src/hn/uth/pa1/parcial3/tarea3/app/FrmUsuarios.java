    private void btnCambiarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarEstadoActionPerformed
        // TODO add your handling code here:
        Usuarios usuario = null;
        try {
            usuario = getUsuarioFormulario();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Campos mal llenados", JOptionPane.ERROR_MESSAGE);
        }
        if (usuario != null) {
            if (usuario.getUsuario().equals("ADMINISTRADOR") == false) {

                String estado = usuario.getEstado();
                if (estado.equals("Activo")) {
                    usuario.setEstado("No Activo");
                }
                if (estado.equals("No Activo")) {
                    usuario.setEstado("Activo");
                }
                try {
                    usuariosController.modificarUsuario(usuario);
                    JOptionPane.showMessageDialog(this, "Estado de usuario modificado con exito", "Exito", JOptionPane.INFORMATION_MESSAGE);
                    vaciarCampos();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "El usuario al que desea modificar el estado, por favor trate de crearlo en lugar de modificarlo",
                            "Registro no Existente", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No puede desactivar al Administrador",
                        "Registro de Administrador", JOptionPane.ERROR_MESSAGE);
            }
        }
        refrescarTabla();
    }//GEN-LAST:event_btnCambiarEstadoActionPerformed

