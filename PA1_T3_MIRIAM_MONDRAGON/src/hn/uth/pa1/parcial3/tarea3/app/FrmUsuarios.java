    private Usuarios getUsuarioFormulario() throws Exception {
        Usuarios usuarioFormulario = null;
        String usuario = txtUsuario.getText();
        String contrasenia = txtClave.getText();
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        String direccion = txtDireccion.getText();
        String telefono = null;
        int digito = 0;
        try {
            char[] telefonoDigitos = txtTelefono.getText().toCharArray();
            if (telefonoDigitos.length == 8) {
                for (int i = 0; i < telefonoDigitos.length; i++) {
                    digito = telefonoDigitos[i];
                    i++;
                }
                telefono = txtTelefono.getText();
            } else {
                throw new Exception("El número de teléfono no es válido, debe ser un número de 8 dígitos");
            }
        } catch (Exception e) {
            throw new Exception("El número de teléfono no es válido, debe ser un número de 8 dígitos");
        }
        String estado = "";
        if (btnCambiarEstado.isVisible() == false || btnCambiarEstado.getText().equals("Desactivar Usuario")) {
            estado = "Activo";
        } else {
            estado = "No Activo";
        }
        if (telefono != null) {
            usuarioFormulario = new Usuarios(usuario, contrasenia, nombres, apellidos, direccion, telefono, estado);
        }

        return usuarioFormulario;
    }

